CREATE DATABASE COFFEEMANAGEMENT;

USE COFFEEMANAGEMENT;

CREATE TABLE CUSTOMERS
(
	CustomerID CHAR(4),
	Phone VARCHAR(20),
	DateRegister SMALLDATETIME,
	TotalSpend MONEY
)

CREATE TABLE INVOICES 
(
	InvoiceID CHAR(4),
	Date DATE,
	Value MONEY,
	CustomerID CHAR(4),
	EmployeeID CHAR(4)
)

CREATE TABLE EMPLOYEES
(
	EmployeeID CHAR(4),
	Phone VARCHAR(20),
	DayBegin SMALLDATETIME,
	Name VARCHAR(40)
)

CREATE TABLE BEVARAGES
(
	BevarageID CHAR(4),
	Size CHAR(1),
	Price MONEY
)

CREATE TABLE CAKES
(
	CakeID CHAR(4),
	Price MONEY,
	MFG SMALLDATETIME,
	EXP SMALLDATETIME
)


CREATE TABLE ITEMS
(
	ItemID CHAR(4),
	Price MONEY
)
